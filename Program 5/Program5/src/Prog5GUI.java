    /**
     * This method adds an instate student to the list. The program does any 
     * exception handling and checks to see if the student is already in the 
     * list. If there is an exception a message is displayed. 
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int flag, credit, scholar;
        String scholarString;
        String id = inID.getText();
        String name = inName.getText();
        if (inCredit.getText().equals(""))
            credit = 0;
        else 
            credit = Integer.parseInt(inCredit.getText());
        if (scholarship.getText().equals(""))
        {
            scholar = 0;
        }
        else 
        {
            scholarString = scholarship.getText();
            scholar = Integer.parseInt(scholarString);
        }
        flag = sList.addInstate(id,name,credit,scholar);
        if (flag == -1)
        {
            outText.setText( outText.getText() + "Student information "
                    + "is invalid! Try again!" + "\n");
        }
        else if (flag == 0)
        {
            outText.setText( outText.getText() + "Student already exists"
                    + "!" + "\n");
        }
        else
        {
            outText.setText( outText.getText() + "Instate student "
                    + "added to the list." + "\n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
