    /**
     * This method calculates the tuition for all reports and displays a string 
     * with student and tuition. If list is empty and error message is output. 
     * @param evt 
     */
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int listSize;
        listSize = sList.returnListSize();
        if (listSize == 0 )
        {
            outText.setText(outText.getText() + "Empty List!" + "\n");
        }
        else 
        {
            outText.setText(outText.getText() + "\n");
            for ( int i = 0; i< listSize; i++)
            {
                outText.setText(outText.getText() + sList.calculateReport(i) 
                        + "\n");
            }
            outText.setText(outText.getText() + "\n");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

